package com.bags;

public class Bag {
    private String jenis;
    private String harga;

    public Bag(String jenis, String harga) {
        this.jenis = jenis;
        this.harga = harga;
    }

    public String getJenis() {
        return jenis;
    }

    public String getHarga() {
        return harga;
    }
}
    